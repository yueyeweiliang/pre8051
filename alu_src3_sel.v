module alu_src3_sel (sel, pc, dptr, out);
//
// sel          select signals (from decoder, delayd one clock)
// pc           program counter input
// dptr         data pointer input
// des          output (alu sorce 2)

input sel;
input [7:0] pc, dptr;
output [7:0] out;

assign out = sel ? pc : dptr;

endmodule
